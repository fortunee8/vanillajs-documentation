//Array declaration
const namaBuah = ['Apel', 'Jeruk', 'Nanas', 'Melon', 'Anggur'];

//Show the array value into console
console.log(namaBuah);

//Add array element into index
namaBuah.push('pisang', 'mangga', 'semangka');
//Check the array lenght after pushing
console.log(namaBuah.length);

//Remove array's index from memory
namaBuah.pop('Apel', 'Nanas');
//Check the array's lenght after popping
console.log(namaBuah.length);